    static func channels(for satellite: Satellite) -> [SatelliteFrequencyItem] {
        if let canonical = canonicalNameByNorad[satellite.id], let items = channelsByName[canonical] {
            return items
        }

        let normalized = normalizeName(satellite.name)
        for (key, items) in channelsByName where normalized.contains(normalizeName(key)) {
            return items
        }
        return []
    }

    private static func normalizeName(_ raw: String) -> String {
        raw.uppercased()
            .replacingOccurrences(of: "-", with: " ")
            .replacingOccurrences(of: "_", with: " ")
            .replacingOccurrences(of: "  ", with: " ")
    }
